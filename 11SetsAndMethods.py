# SETS is a collection of different thing
a1 = {3, 5, 22, 4, 5}
a2 = {3, 5, 220, 42, 5}
# print(a1)
# print(a1.pop())
a1.add(2)                     # add the value at the end of the set
print(a1)

print(a1.union(a2))           # give SIMILAR record from a1 and a2
print(a1.intersection(a2))    # COMMON elements from a1 and a2

